sc = input("Enter a Letter (a, e, i, o, u) : ")

if sc == "a" or sc == "e" or sc == "i" or sc == "o" or sc == "u":
    print(f"{sc} is a vowel")
elif sc == "y":
    print(f"{sc} can be both a vowel or a consonant")
else:
    print(f"{sc} is a consonant")








